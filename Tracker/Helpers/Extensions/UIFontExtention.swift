import UIKit

extension UIFont {
    static var ypBoldSize34: UIFont {
        return UIFont.systemFont(ofSize: 34, weight: .bold)
    }
    
    static var ypBoldSize32: UIFont {
        return UIFont.systemFont(ofSize: 32, weight: .bold)
    }
    
    static var ypBoldSize19: UIFont {
        return UIFont.systemFont(ofSize: 19, weight: .bold)
    }
    
    static var ypRegularSize17: UIFont {
        return UIFont.systemFont(ofSize: 17, weight: .regular)
    }
    
    static var ypMediumSize16: UIFont {
        return UIFont.systemFont(ofSize: 16, weight: .medium)
    }
    
    static var ypMediumSize12: UIFont {
        return UIFont.systemFont(ofSize: 12, weight: .medium)
    }
    
    static var ypMediumSize10: UIFont {
        return UIFont.systemFont(ofSize: 10, weight: .medium)
    }
}
